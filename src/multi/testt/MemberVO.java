package multi.testt;

public class MemberVO {

    private String name;
    private int age;
    private String email;

    // 이름에 대한 getter 메서드
    public String getName() {
        return name;
    }

    // 이름에 대한 setter 메서드
    public void setName(String name) {
        this.name = name;
    }

    // 나이에 대한 getter 메서드
    public int getAge() {
        return age;
    }

    // 나이에 대한 setter 메서드
    public void setAge(int age) {
        this.age = age;
    }

    // 이메일에 대한 getter 메서드
    public String getEmail() {
        return email;
    }

    // 이메일에 대한 setter 메서드
    public void setEmail(String email) {
        this.email = email;
    }

}
